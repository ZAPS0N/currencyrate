declare(strict_types=1);

namespace CurrencyRate\Service;

/**
 * HTTP Client interface for making API requests
 */
interface HttpClientInterface
{
    /**
     * Make GET request to specified URL
     *
     * @param string $url Request URL
     * @param array $headers Optional headers
     * @param int $timeout Request timeout in seconds
     *
     * @return array|false Response data or false on failure
     *
     * @throws \Exception On HTTP or network errors
     */
    public function get(string $url, array $headers = [], int $timeout = 10);
}
