// Load Composer autoloader if available
if (file_exists(__DIR__ . '/vendor/autoload.php')) {
    require_once __DIR__ . '/vendor/autoload.php';
}

// PSR-4 Autoloader for CurrencyRate namespace
spl_autoload_register(function ($class) {
    $prefix = 'CurrencyRate\\';
    $baseDir = __DIR__ . '/src/';

    $len = strlen($prefix);
    if (strncmp($prefix, $class, $len) !== 0) {
        return;
    }

    $relativeClass = substr($class, $len);
    $file = $baseDir . str_replace('\\', '/', $relativeClass) . '.php';

    if (file_exists($file)) {
        require_once $file;
    }
});

// Fallback: manually load legacy classes for backward compatibility
if (!class_exists('CurrencyRateModel')) {
    require_once __DIR__ . '/classes/CurrencyRateModel.php';
}

if (!class_exists('CurrencyRateRepository')) {
    require_once __DIR__ . '/classes/CurrencyRateRepository.php';
}
